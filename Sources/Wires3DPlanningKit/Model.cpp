#include "Model.h"

#include <algorithm>

void Model::AddWall(const ModelWall& model_wall)
{
    m_walls.push_back(model_wall);
}
///////////////////////////////////////////////////////////////////////////////////////////////////

void Model::AddSocket(const ModelSocket& model_socket)
{
    m_sockets.push_back(model_socket);
}
///////////////////////////////////////////////////////////////////////////////////////////////////

void Model::RemoveWall(const size_t wall_index)
{
    m_walls.erase(m_walls.begin() + wall_index);
}
///////////////////////////////////////////////////////////////////////////////////////////////////

void Model::RemoveSocket(const size_t socket_index)
{
    m_sockets.erase(m_sockets.begin() + socket_index);
}
///////////////////////////////////////////////////////////////////////////////////////////////////

size_t Model::GetWallsNumber()
{
    return m_walls.size();
}
///////////////////////////////////////////////////////////////////////////////////////////////////

size_t Model::GetSocketsNumber()
{
    return m_sockets.size();
}
///////////////////////////////////////////////////////////////////////////////////////////////////